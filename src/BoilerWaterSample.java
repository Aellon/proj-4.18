public class BoilerWaterSample extends Sample{
    private double ph,tds,hydrazene,sugarTrace,alkalinityP,alkalinityO,phosphate;

    public double getPh() {
        return ph;
    }

    public void setPh(double ph) {
        this.ph = ph;
    }

    public double getTds() {
        return tds;
    }

    public void setTds(double tds) {
        this.tds = tds;
    }

    public double getHydrazene() {
        return hydrazene;
    }

    public void setHydrazene(double hydrazene) {
        this.hydrazene = hydrazene;
    }

    public double getSugarTrace() {
        return sugarTrace;
    }

    public void setSugarTrace(double sugarTrace) {
        this.sugarTrace = sugarTrace;
    }

    public double getAlkalinityP() {
        return alkalinityP;
    }

    public void setAlkalinityP(double alkalinityP) {
        this.alkalinityP = alkalinityP;
    }

    public double getAlkalinityO() {
        return alkalinityO;
    }

    public void setAlkalinityO(double alkalinityO) {
        this.alkalinityO = alkalinityO;
    }

    public double getPhosphate() {
        return phosphate;
    }

    public void setPhosphate(double phosphate) {
        this.phosphate = phosphate;
    }
    
    
}
